function TabellaNascondereMostrare() {
    const tabella = document.getElementById("__");
  
    if (tabella.style.display === "none") {
      tabella.style.display = "block";
    } else {
      tabella.style.display = "none";
    }
  }
  
  function DivNascondereMostrare() {
    const div = document.getElementById("DivNascondereMostrare");
  
    if (div.style.display === "none") {
      div.style.display = "block";
    } else {
      div.style.display = "none";
    }
  }

  
  
